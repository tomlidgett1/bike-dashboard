// ============================================================================
// Entities — page-per-entity model (people/places/orgs/topics)
//
// memory.ts handles facts about the user (lives in Melbourne, vegetarian).
// This module handles everyone/everything else in the user's life:
//
//   "Had coffee with Sarah" -> Sarah's person page gets a timeline entry
//   "Acme paid the invoice" -> Acme's org page bumps mention_count
//   "Going back to Brunetti tomorrow" -> Brunetti place page recorded
//
// The compiled_truth on each entity gets injected into prompts so the
// agent answers "how's Sarah?" with actual context instead of "who?".
//
// Pattern inspired by GBrain (Garry Tan): compiled truth above the line,
// append-only timeline below the line, regenerated by background jobs.
// ============================================================================

