const FIELD_BY_KEY = new Map(SALES_TABLE_FIELDS.map((field) => [field.key, field]));

export function getSalesField(key: string): TableBuilderField | undefined {
  return FIELD_BY_KEY.get(key);
}

export function getSalesFieldsForGrain(grain: TableBuilderGrain): TableBuilderField[] {
  return SALES_TABLE_FIELDS.filter((field) => field.grains.includes(grain));
}

export function groupSalesFields(
  fields: TableBuilderField[],
): Array<{ group: string; fields: TableBuilderField[] }> {
  const order: string[] = [];
  const map = new Map<string, TableBuilderField[]>();

  for (const field of fields) {
    if (!map.has(field.group)) {
      map.set(field.group, []);
      order.push(field.group);
    }
    map.get(field.group)!.push(field);
  }

  return order.map((group) => ({ group, fields: map.get(group)! }));
}

