const BOTH: TableBuilderGrain[] = ["sale", "sale_line"];
const SALE_ONLY: TableBuilderGrain[] = ["sale"];
const LINE_ONLY: TableBuilderGrain[] = ["sale_line"];

function f(
  partial: Omit<TableBuilderField, "grains"> & { grains?: TableBuilderGrain[] },
): TableBuilderField {
  return {
    grains: BOTH,
    ...partial,
  };
}

